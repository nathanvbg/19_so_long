#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	char	*s1;
	char	*dst;

	i = 0;
	s1 = (char *)str;
	dst = malloc(ft_strlen(s1) + 1 * sizeof(char));
	if (!dst)
		return (NULL);
	while (s1[i])
	{
		dst[i] = s1[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
